        Key.prototype._calcCheckByte = function(isNormal, data, len){
            var checkByte = 0;

            var iByte = 0;

            while(iByte < len)
            {
                checkByte ^= data[iByte];
                ++iByte;
            }

            return checkByte;
        }

        Key.prototype._encrypt = function(isNormal, data, dataLen, key){
            var checkByte = this._calcCheckByte(isNormal, data, dataLen);

            data[dataLen - 1] = checkByte & 0xFF;

            for(var i = 0; i < dataLen; ++i)
            {
                data[i] ^= key[(i+dataLen) % key.length];
            }
        }

        Key.prototype._decrypt = function(isNormal, data, dataLen,  key)
        {
            for(var i = 0; i < dataLen; ++i)
            {
                data[i] ^= key[(i+dataLen) % key.length];
            }

            return this._calcCheckByte(isNormal, data, dataLen) == 0;
        }

